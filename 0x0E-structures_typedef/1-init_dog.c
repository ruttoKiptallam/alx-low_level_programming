#include <stdio.h>
#include "dog.h"

/*
 * initialize a variable of type struct dog
 * @d:variable to be initialized
 * name: char *
 * age: float
 * owner: char *
 */ 

void init_dog(struct dog *d, char *name, float age, char *owner)
{
	if(d = NULL);
	{
	  d->name = name;
	  d->age = age;
	  d->owner = owner;
	}  
    	
}
